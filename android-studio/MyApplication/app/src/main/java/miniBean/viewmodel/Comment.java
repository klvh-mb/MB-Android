package miniBean.viewmodel;

public class Comment {
    public Long oid;
    public String on;
    public Long cd;
    public String d;

    public String getOn() {
        return on;
    }

    public void setOn(String on) {
        this.on = on;
    }

    public Long getCd() {
        return cd;
    }

    public void setCd(Long cd) {
        this.cd = cd;
    }

    public Long getOid() {
        return oid;
    }

    public void setOid(Long oid) {
        this.oid = oid;
    }

    public String getD() {
        return d;
    }

    public void setD(String d) {
        this.d = d;
    }
}
