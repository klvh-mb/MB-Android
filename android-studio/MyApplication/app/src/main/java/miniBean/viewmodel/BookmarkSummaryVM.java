package miniBean.viewmodel;

public class BookmarkSummaryVM {
    public boolean b;
    public long qc;
    public long pc;
    public long ac;
    public long pkc;

    public boolean isB() {
        return b;
    }

    public void setB(boolean b) {
        this.b = b;
    }

    public long getQc() {
        return qc;
    }

    public void setQc(long qc) {
        this.qc = qc;
    }

    public long getPc() {
        return pc;
    }

    public void setPc(long pc) {
        this.pc = pc;
    }

    public long getAc() {
        return ac;
    }

    public void setAc(long ac) {
        this.ac = ac;
    }

    public long getPkc() {
        return pkc;
    }

    public void setPkc(long pkc) {
        this.pkc = pkc;
    }
}
